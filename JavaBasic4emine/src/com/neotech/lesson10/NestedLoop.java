package com.neotech.lesson10;

public class NestedLoop {
	public static void main(String[] args) {

		// iterate through a number 5 times

		for (int i = 0; i <= 5; i++) // ------->OUTER LOOP

		{

			for (int a = 0; a <= 5; a++) {
				System.out.println("i: " + i + "a:" + a);
				;
				;
				;
				;
				;
				;
				;
				;
				;
				;
				;
			}

		}

	}

}
