package com.neotech.lesson08;

public class IncrementAndDicrement {
	
	public static void main(String[] args) {
		
		int x=10;
		
		//re-assign
		x=11;
		
		x=x+1;
		
		//shorthand operator
		
		x+=1;
		//increment
		x++;
		System.out.println(x);
		
		int y=50;
		y=y-1;
		//shorthand op
		
		y-=1;
		
		y--;
		
		System.out.println(y);
		
		
		
		
	}

}
